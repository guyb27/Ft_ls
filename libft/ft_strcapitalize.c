#include "libft.h"

char	*ft_strcapitalize(char *s)
{
	int i;

	i = 0;
	while (s[i++])
	{
		if (s[i] >= 65 && s[i] <= 90)
			s[i] = s[i] + 32;
	}
	i = 0;
	if (s[i] >= 97 && s[i] <= 122)
		s[i] = s[i] - 32;
	while (s[++i])
	{
		if (s[i] >= 97 && s[i] <= 122 && s[i - 1] >= 0 && s[i - 1] <= 47)
			s[i] = s[i] - 32;
	}
	return (s);
}
