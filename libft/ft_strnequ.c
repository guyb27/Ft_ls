#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (!s1 && !s2 ? 1 : 0);
	return (ft_strncmp(s1, s2, n) ? 0 : 1);
}
