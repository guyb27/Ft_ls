#include "libft.h"

char	*ft_strtrim(const char *s)
{
	size_t i;
	size_t j;

	i = 0;
	if (s == 0)
		return (NULL);
	j = ft_strlen(s) - 1;
	while ((s[i] == ' ' || s[i] == '\n' || s[i] == '\t') && s[i] != '\0')
		i++;
	while ((s[j] == ' ' || s[j] == '\n' || s[j] == '\t') && j > 0)
		j--;
	j = s[i] == '\0' ? -1 : j;
	i = s[i] == '\0' ? 0 : i;
	return (ft_strsub(s, i, (j - i) + 1));
}
