#include "libft.h"

void			ft_free_tab(char ***tab)
{
	int		i;

	i = 0;
	if ((*tab))
	{
		while ((*tab)[i])
		{
			free((*tab)[i]);
			(*tab)[i] = NULL;
			i++;
		}
		free(*tab);
		*tab = NULL;
	}
}
